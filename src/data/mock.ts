/** Helper to build a GalpaoEstoque entry */
function est(id: number, nome: string, saldo: number, reservado: number, qtdPedida: number, localizacao?: string): GalpaoEstoque {
  const disponivel = Math.max(0, saldo - reservado);
  return {
    deposito: { id, nome, saldo, reservado, disponivel },
    atende: disponivel >= qtdPedida,
    localizacao,
  };
}

